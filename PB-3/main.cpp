#include <iostream>

using namespace std;

int main()
{

    int num1 = 2, num2 = 2;

    cout << "Result using +: " << num1 + num2 << endl;

    cout << "Result using -: " << num1 - num2 << endl;

    cout << "Result using *: " << num1 * num2 << endl;

    cout << "Result using /: " << num1 / num2 << endl;

    return 0;
}
